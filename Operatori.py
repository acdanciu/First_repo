'''
Operatori:
aritmetici: +,-,*,/,%
de comparare: < > , ==, !=, >=, <=
logici: &&, ||, !
'''

a = 3
b = 5

print(a + b) # 3+5 =>8
print(a % b) # 3%5 =>3
print(5 % b) # 3%5 =>0
print(a == b) # a este egal cu b? => false
print(a<b and a<4) # true si true => true
print(a<b or a<2) # true sau false => true

# cu mama sau tata sau (cu bunicu si bunica)
